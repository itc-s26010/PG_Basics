#「スライス」リストや文字列の部分的な抽出を行うこと
fict = ["トルストイ","カミュ","オーウェル","ハクスリー","オースティン"]
print(fict[0:3])  #インデックス番号0番目以降から3番目の手前まで

#文字列のスライス指定のいろいろな例
ivan = "死の代わりにひとつの光があった。"
print(ivan[0:6])   #インデックス番号0番から6番目の手前まで
print(ivan[6:16])  #インデックス番号6番から16番目の手前まで
print(ivan[:6])    #開始インデックス(0番目から)を省略
print(ivan[6:])    #終了インデックスを省略(最後の要素まで)
print(ivan[:])     #文字列の最初から文字列の最後まで?(文字列のコピー)
