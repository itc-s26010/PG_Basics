#文字列の変換　※「使っているときだけ」変換
#.upper() 大文字にする
print("We hold these truths...".upper())

#.lower() 小文字にする
print("SO IT GOES.".lower())

#.captalize() 頭文字だけ大文字にする
print("four score and...".capitalize())

#変数で実験すると変数の中身は変わっていない
mand = "This is the way."
print(mand)
print(mand.upper())
print(mand.lower())
print(mand)
