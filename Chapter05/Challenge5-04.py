#自分の特徴を記述した辞書
miyagi_you = {
        "名前":"宮城 葉",
        "誕生日":"７月１７日",
        "スキな色":"中間色",
        "好きな音楽":"柊キライシリーズ",
        "弱点":"コミュ障"}
#調べたい特徴を尋ねる
ask = input("知りたい特徴を入力してください>")
#入力された特徴が辞書にあるか
if ask in miyagi_you:
    answer = miyagi_you[ask]
    print(answer)
else:
    print("それはわからないです。今度聞いておきます")
